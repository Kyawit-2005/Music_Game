	public static void MovePlayer(String Direction) {
		try{
			switch(Direction) {
				case "UP":
					TileTrigger(MapGrid[PosY-1][PosX]);
					if(canMove) {
						PosY--;
					} else System.out.println("The position above you is a Block");
					break;
					
				case "DOWN":
					TileTrigger(MapGrid[PosY+1][PosX]);
					if(canMove) {
						PosY++;
					} else System.out.println("The position below you is a Block");
					break;
					
				case "LEFT":
					TileTrigger(MapGrid[PosY][PosX-1]);
					if(canMove) {
						PosX--;
					} else System.out.println("The position to the left of you is a Block");
					break;
					
				case "RIGHT":
					TileTrigger(MapGrid[PosY][PosX+1]);
					if(canMove) {
						PosX++;
					} else System.out.println("The position to the right of you is a Block");
					break;
					
				default:
			};
		} catch(ArrayIndexOutOfBoundsException err) {
			System.out.println("[ArrayIndexOutOfBoundsException] OUT OF BOUNDS!!!");
			canMove = false;
		}
	}
	
	public static void TileTrigger(int num) {
		switch(num) {
			case 0: case 1:
				canMove = true; break;
			case 2: 
				canMove = false; break;
			case 3: 
				JOptionPane.showMessageDialog(null, "You picked up a Key.\nYou wonder what it is for...", "Sample Trigger", 1);
				KeyAmount++;
				canMove = true;
				break;
			case 4: 
				String[] Choice = {"Open the Door ["+KeyAmount+" key(s)]", "Save the key for now..."};
				int chosen = JOptionPane.showOptionDialog(null, "This door is locked. Want to open the door?", "Sample Trigger", 0, 1, null, Choice, Choice[0]);
				if(chosen == 0 && KeyAmount <= 0) {
					JOptionPane.showMessageDialog(null, "You don't have enough keys.", "Sample Trigger", 0);
					canMove = false;
					break;
				} else if(chosen == 0) {
					JOptionPane.showMessageDialog(null, "You successfully opened the door...", "Sample Trigger", -1);
					KeyAmount--;
					canMove = true;
				} else canMove = false;
				break;
			case 9: 
				JOptionPane.showMessageDialog(null, "This is an Exit Door...", "Sample Trigger", 1);
				canMove = true; 
				break;
			case 11: 
				JOptionPane.showMessageDialog(null, "You will now proceeding to Playing \"Music Enhypen\"", "Sample Trigger", 1);
				canMove = false; 
				//Enter another Java file here
				
				Main.Processes[4].setVisible(true);
				
				break;
			default: //nothing
		}; DebugTileInfo(num);
	}
	
